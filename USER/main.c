void sendNum(u16 num, u8 usartNum){ //Output in decimal form.
	u8 length=0 , i=0 ;
	bits[1]=0 ;
	while(num){
		bits[++length] = num%10 ;
		num/=10 ;
	}
	if(length==0) length=1 ;
	for(i=length ; i>=1 ; --i){
		if(usartNum==1){
			USART1->DR = bits[i]+'0';
			while((USART1->SR & 0x40) == 0);
		}
		else if(usartNum==2){
			USART2->DR = bits[i]+'0';
			while((USART2->SR & 0x40) == 0);
		}
	}
}
		 
